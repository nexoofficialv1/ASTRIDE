import assert from 'node:assert/strict';
import fs from 'node:fs';
const read=(p)=>fs.readFileSync(p,'utf8');
const base='apps/driver_flutter/lib/screens';
const dashboard=read(`${base}/astride_driver_dashboard.dart`);
const request=read(`${base}/new_ride_request_screen.dart`);
const active=read(`${base}/ride/active_ride_screen.dart`);
const performance=read(`${base}/performance/driver_performance_screen.dart`);
const shell=read(`${base}/driver_shell.dart`);
for (const token of ['youAreOnline','acceptanceRate','rejectedRides','onlineHours','quickActions']) assert.ok(dashboard.includes(token),`dashboard missing ${token}`);
for (const token of ['cashOrUpi','safeRide','fullToto','pickupEta']) assert.ok(request.includes(token),`request missing ${token}`);
for (const token of ['freeWaiting','passengerOtp','navigateToPickup','emergencySos']) assert.ok(active.includes(token),`active ride missing ${token}`);
for (const token of ['promoterCoaching','cancellationRate','weeklyTrend']) assert.ok(performance.includes(token),`performance missing ${token}`);
assert.ok(shell.includes('DriverPerformanceScreen'));
const locales=['en','bn','hi'].map(c=>JSON.parse(read(`apps/driver_flutter/assets/locales/${c}.json`)));
assert.equal(Object.keys(locales[0]).length,Object.keys(locales[1]).length);
assert.equal(Object.keys(locales[1]).length,Object.keys(locales[2]).length);
for(const key of ['gpsReady','cashOrUpi','freeWaiting','promoterCoaching']) for(const locale of locales) assert.ok(locale[key]);
console.log(`Driver UI Sprint 5 contract passed with ${Object.keys(locales[0]).length} locale keys.`);
