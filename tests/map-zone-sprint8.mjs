import assert from 'node:assert/strict';
import { evaluateZoneContext, validateRideAgainstZone, pointInPolygon } from '../services/api/src/domain/zone-engine.mjs';
import { listZones, upsertZone } from '../services/api/src/store/zone-store.mjs';
import { getMapAdapter } from '../services/api/src/providers/maps/index.mjs';
const service=listZones().find(z=>z.type==='SERVICE_AREA');
assert.ok(service);
assert.equal(pointInPolygon({lat:23.22,lng:88.35},service.polygon),true);
const inside=evaluateZoneContext({pickup:{lat:23.22,lng:88.35},destination:{lat:23.23,lng:88.36},zones:listZones()});
assert.equal(inside.isOutsideArea,false);
const outside=evaluateZoneContext({pickup:{lat:23.22,lng:88.35},destination:{lat:23.30,lng:88.45},zones:listZones()});
assert.equal(outside.isOutsideArea,true);
assert.equal(validateRideAgainstZone({rideType:'SHARE_TOTO',distanceKm:12,zoneContext:outside}).allowed,false);
assert.equal(validateRideAgainstZone({rideType:'FULL_TOTO',distanceKm:12,zoneContext:outside}).allowed,true);
assert.equal(validateRideAgainstZone({rideType:'FULL_TOTO',distanceKm:30,zoneContext:outside}).reason,'TOTO_MAXIMUM_DISTANCE_EXCEEDED');
upsertZone({code:'RISK_TEST',name:'Risk Test',type:'HIGH_RISK',polygon:[{lat:23.21,lng:88.34},{lat:23.23,lng:88.34},{lat:23.23,lng:88.37},{lat:23.21,lng:88.37}]});
const risk=evaluateZoneContext({pickup:{lat:23.22,lng:88.35},destination:{lat:23.23,lng:88.36},zones:listZones()});
assert.equal(risk.highRiskZone,true);
const route=await getMapAdapter('mappls').route({lat:23.22,lng:88.35},{lat:23.23,lng:88.36});
assert.ok(route.distanceM>0);assert.ok(route.durationS>0);
console.log('Sprint 8 map/zone/navigation tests passed');
